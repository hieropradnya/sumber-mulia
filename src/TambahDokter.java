    private void jButtonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTambahActionPerformed
        // TODO add your handling code here:
        // Pengecekan kondisi sebelum menambahkan data
        if (checkNama() && checkNomorHP() && checkJenisKelamin() && checkPoli()) {
            // Jika semua kondisi terpenuhi, tambahkan data
            nama = jTextFieldNamaDokter.getText();
            nomor_hp = jTextFieldNomorHP.getText();
            jenis_kelamin = jComboBoxJenisKelamin.getSelectedItem().toString();
            poli = jComboBoxPoli.getSelectedItem().toString();
            
            insertData();
            clearForm();
            

            // Format kalimat dengan data dokter
            kalimat = "Data Dokter" +
                    "\n\nNama           : " + nama +
                    "\nNomor HP       : " + nomor_hp +
                    "\nJenis Kelamin  : " + jenis_kelamin +
                    "\nPoli           : " + poli +
                    "\n\nBerhasil Ditambahkan";

            // Update tampilan
            jTextAreaTampil.setText(kalimat);
        } else{
            kalimat = "Masukkan data dengan benar!";
            jTextAreaTampil.setText(kalimat);
        }
    }//GEN-LAST:event_jButtonTambahActionPerformed

