    private boolean checkNama() {
        nama = jTextFieldNamaDokter.getText();
        String errorNama = "ERROR: Nama hanya boleh terdiri dari huruf.";

        boolean isValidNama = nama.matches("[a-zA-Z\\s]+");

        if (!isValidNama) {
            jTextAreaTampil.setText(errorNama);
        }

        return isValidNama;
    }

    private boolean checkNomorHP() {
        nomor_hp = jTextFieldNomorHP.getText();
        String errorNomorHP = "ERROR: Nomor HP hanya boleh terdiri dari angka.";

        boolean isValidNomorHP = nomor_hp.matches("[0-9]+");

        if (!isValidNomorHP) {
            jTextAreaTampil.setText(errorNomorHP);
        }

        return isValidNomorHP;
    }

    private boolean checkJenisKelamin() {
        String selectedJenisKelamin = jComboBoxJenisKelamin.getSelectedItem().toString();
        String errorJenisKelamin = "ERROR: Pilih jenis kelamin yang valid.";

        if ("Pilih jenis kelamin".equals(selectedJenisKelamin)) {
            jTextAreaTampil.setText(errorJenisKelamin);
            return false;
        }

        return true;
    }

    private boolean checkPoli() {
        String selectedPoli = jComboBoxPoli.getSelectedItem().toString();
        String errorPoli = "ERROR: Pilih poli yang valid.";

        if ("Pilih poli".equals(selectedPoli)) {
            jTextAreaTampil.setText(errorPoli);
            return false;
        }

        return true;
    }
    
    private boolean insertData(){
        try{
            stmt = conn.createStatement();
            int insert = stmt.executeUpdate("INSERT INTO dokter(nama, nomor_hp, jenis_kelamin, poli) VALUES('" + 
                    nama + "', '" + nomor_hp + "', '" + jenis_kelamin + "', '" + poli + "')");

            if(insert == 1){
                JOptionPane.showMessageDialog(rootPane, "Data tersimpan.");
            }
            return true;
        } catch (SQLException ex) {
            // Tangani kesalahan SQL
            JOptionPane.showMessageDialog(null, "Kesalahan SQL: " + ex.getMessage());
        }
        return false;
    }
    
    private void clearForm(){
        jTextFieldNamaDokter.setText("");
        jTextFieldNomorHP.setText("");
        jComboBoxJenisKelamin.setSelectedIndex(0);
        jComboBoxPoli.setSelectedIndex(0);
    }


